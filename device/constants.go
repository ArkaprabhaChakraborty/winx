// CTL_CODE constructs an IOCTL code
func CTL_CODE(deviceType, function, method, access uint32) uint32 {
	return (deviceType << 16) | (access << 14) | (function << 2) | method
}

// Common IOCTL codes
const (
	// Disk IOCTLs
	IOCTL_DISK_GET_DRIVE_GEOMETRY = (FILE_DEVICE_DISK << 16) | (FILE_ANY_ACCESS << 14) | (0 << 2) | METHOD_BUFFERED
	IOCTL_DISK_GET_PARTITION_INFO = (FILE_DEVICE_DISK << 16) | (FILE_READ_ACCESS << 14) | (1 << 2) | METHOD_BUFFERED
	IOCTL_DISK_GET_DRIVE_LAYOUT   = (FILE_DEVICE_DISK << 16) | (FILE_READ_ACCESS << 14) | (3 << 2) | METHOD_BUFFERED

	// Storage IOCTLs
	IOCTL_STORAGE_GET_DEVICE_NUMBER = (FILE_DEVICE_MASS_STORAGE << 16) | (FILE_ANY_ACCESS << 14) | (0x420 << 2) | METHOD_BUFFERED
	IOCTL_STORAGE_QUERY_PROPERTY    = (FILE_DEVICE_MASS_STORAGE << 16) | (FILE_ANY_ACCESS << 14) | (0x500 << 2) | METHOD_BUFFERED

	// Volume IOCTLs
	IOCTL_VOLUME_GET_VOLUME_DISK_EXTENTS = (0x00000056 << 16) | (FILE_ANY_ACCESS << 14) | (0 << 2) | METHOD_BUFFERED
)

